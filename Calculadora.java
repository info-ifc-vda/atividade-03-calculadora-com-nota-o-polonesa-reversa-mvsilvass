    private void onClickOperation(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_onClickOperation
        JButton button = (JButton) evt.getSource();
        String operation = button.getText();

        String userInput = this.display.getText();

        if (!userInput.isBlank()) {
            char lastChar = userInput.charAt(userInput.length() - 1);
            if (Character.isDigit(lastChar)) {
                this.display.setText(userInput + operation);
            } else {
                String updatedDisplay = userInput.substring(0, userInput.length() - 1);
                this.display.setText(updatedDisplay + operation);
            }
        } else {
            if (operation.equals("-")) {
                this.display.setText("-");
            }
        }

    }// GEN-LAST:event_onClickOperation

