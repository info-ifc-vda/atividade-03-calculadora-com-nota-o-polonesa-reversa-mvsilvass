    private void onClickComma(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onClickComma
        String userInput = this.display.getText();

        if (!userInput.isEmpty()) {
            String[] parts = userInput.split("[-+*/]");
            String lastNumber = parts[parts.length - 1];

            if (!lastNumber.contains(",")) {
                char lastChar = userInput.charAt(userInput.length() - 1);
                if (operations.contains(String.valueOf(lastChar))) {
                    this.display.setText(userInput + "0,");
                } else {
                    this.display.setText(userInput + ",");
                }
            }
        } else {
            this.display.setText("0,");
        }


    }//GEN-LAST:event_onClickComma

