    private void updateStyle() {
        String userInput = this.display.getText();

        if (!userInput.isBlank()) {
            char lastChar = userInput.charAt(userInput.length() - 1);

            if (Character.isDigit(lastChar)) {
                setDefaultStyle();
            } else {
                setErrorStyle();
            }
        } else {
            setErrorStyle();
        }

    }

    private Double calculate(String operation, Double number1, Double number2) {
        switch (operation) {
            case "+":
                return number1 + number2;
            case "-":
                return number1 - number2;
            case "*":
                return number1 * number2;
            case "/":
                if (number2 == 0) {
                    setErrorStyle();
                    return 0.0;
                }

                return number1 / number2;
            default:
                return 0.0;
        }

    }

    private int prioridade(String op) {
        if (op.equals("*") || op.equals("/")) {
            return 1;
        }

        return 0;
    }

    private List<String> getPostFixExpression() {
        String text = this.display.getText().replace(",", ".");
        String[] input = text.split("(?<=[-+*/])|(?=[-+*/])");

        List<String> inputList = new ArrayList<>(Arrays.asList(input));
        List<String> outputList = new ArrayList<>();
        List<String> operationsList = new ArrayList<>();

        if (!inputList.isEmpty()) {
            if (Character.isDigit(inputList.getLast().charAt(0))) {
                if (inputList.getFirst().equals("-")) {
                    inputList.addFirst("0");
                }

                for (String character : inputList) {
                    if (this.operations.contains(character)) {
                        while (!operationsList.isEmpty() && prioridade(
                                operationsList.getLast()) >= prioridade(character)) {
                            outputList.add(operationsList.removeLast());
                        }

                        operationsList.add(character);
                    } else {
                        outputList.add(character);
                    }
                }

                while (!operationsList.isEmpty()) {
                    outputList.add(operationsList.removeLast());
                }

                System.out.println("Lista DEPOIS: " + outputList);
                return outputList;

            }

        }

        return new ArrayList<>();
    }

    private void onResult(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_onResult
        this.updateStyle();

        List<String> expression = this.getPostFixExpression();

        for (String character : expression) {
            if (operations.contains(character)) {
                Double num2 = Double.parseDouble(stack.pop());
                Double num1 = Double.parseDouble(stack.pop());
                Double result = calculate(character, num1, num2);
                stack.push(String.valueOf(decimalFormat.format(result)));
            } else {
                stack.push(character);
            }
        }

        if (!expression.isEmpty()) {
            this.display.setText(stack.pop());
        }

        this.stack.clear();

    }// GEN-LAST:event_onResult

